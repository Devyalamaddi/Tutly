import { getCourseClasses } from "@/actions/courses";
import "@/styles/globals.css";
import type { Metadata } from "next";
import Link from "next/link";

export const metadata: Metadata = {
    title: "Classes",
    description: "Generated by create next app",
};

export default async function RootLayout({
    children,
    params
}: Readonly<{
    children: React.ReactNode;
    params:any
}>) {
    const classes = await getCourseClasses(params.id)
    return (
        <>
            <div className="flex">
                <div className="flex flex-col px-4 py-3 gap-2 shadow-xl min-h-[90vh]">
                    <h1 className="px-4">HTML</h1>
                    {
                        classes.map((x) => {
                            return (
                                <Link key={x.id} href={`/courses/${params.id}/class/${x.id}`} className="px-4 py-2 cursor-pointer rounded hover:bg-primary-900">{x.title}</Link>
                            )
                        })
                    }
                </div>
                <div>{children}</div>
            </div>
        </>
    );
}

